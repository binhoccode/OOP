def print_beam_4x4():
    print('+ - - - - + - - - - + - - - - + - - - - +')

def print_post_4x4():
    print('|         |         |         |         |')
def print_four_posts_4x4():
    print_post_4x4()
    print_post_4x4()
    print_post_4x4()
    print_post_4x4()
def draw_grid_4x4():
    print_beam_4x4()
    print_four_posts_4x4()
    
    print_beam_4x4()
    print_four_posts_4x4()
    
    print_beam_4x4()
    print_four_posts_4x4()
    
    print_beam_4x4()
    print_four_posts_4x4()
    
    print_beam_4x4()
print("\n--- 4x4 Grid ---")
draw_grid_4x4()